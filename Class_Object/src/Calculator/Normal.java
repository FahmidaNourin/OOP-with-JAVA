package Calculator;

public class Normal {

	static float Add(float a, float b) { // this is a method
		return a + b;
	}

	static float Sub(float a, float b) { // this is a method
		return a - b;
	}

	static float Mul(float a, float b) { // this is a method
		return a * b;
	}

	static float Div(float a, float b) { // this is a method
		return a / b;
	}

}
