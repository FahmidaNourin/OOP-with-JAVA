package Mini_Calculator;

public abstract class Normal {
	abstract double Add(double x,double y);
	abstract double Add(double x,double y,double z);
	abstract double Add(double x,double y,double p,double t);
	
	abstract double Sub(double x,double y);
	abstract double Sub(double x,double y,double z);
	abstract double Sub(double x,double y,double p,double t);
	
	abstract double Mul(double x,double y);
	abstract double Mul(double x,double y,double z);
	abstract double Mul(double x,double y,double p,double t);
	
	abstract double Div(double x,double y);
	abstract double Div(double x,double y,double z);
	abstract double Div(double x,double y,double p,double t);
	
	

}
